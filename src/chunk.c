#include "chunk.h"
#include "tag.h"

void
music_chunk_init(struct music_chunk *chunk)
{
	chunk->length = 0;
	chunk->tag = NULL;
}

void
music_chunk_free(struct music_chunk *chunk)
{
	if (chunk->tag != NULL)
		tag_free(chunk->tag);
}
